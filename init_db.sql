  street_lighting BOOLEAN NULL,
  pavement_defects BOOLEAN NULL,
  winter_conditions BOOLEAN NULL,
  traffic_light BOOLEAN NULL,
  drainage_system BOOLEAN NULL,
  road_narrowing BOOLEAN NULL,
  bus_stop_elements BOOLEAN NULL,
  road_works_zone_tsod BOOLEAN NULL,
  faults_consolidated TEXT NULL,
  light_condition TEXT NULL,
  road_category TEXT NULL,
  CONSTRAINT road_kart_id_fkey FOREIGN KEY (kart_id) REFERENCES accidents (kart_id)
